void solve(int s_r, int s_c)
{
    for (int k = 0; k < 20; k++)
    {
        int total = 0;
        for(int r = 0; r < 4; r++)
        {
            for(int c = 0; c < 4; c++)
            {
                if (block[k][r][c] == '1' && board[r + s_r][c + s_c] > 0)
                    total += board[r + s_r][c + s_c];
            }
        }
        answer = max(answer, total);
    }
}
