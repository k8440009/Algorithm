void solve(int s_r, int s_c)
{
    for(int i = 0; i < 20; i++)
    {
        int total = 0;
        for(int r = 0; r < 4; r++)
        {
            for(int c = 0; c < 4; c++)
            {
                if(block[i][r][c] == '1' && board[r + s_r][c + s_c] > 0)
                    total += board[r + s_r][c + s_c];
            }
        }
         answer = max(answer, total);
    }
}
