import datetime

def solution(lines):
	answer = 0
	end_times, start_times = [], []
	for i in lines:
		tmp = i.split()
		# et = 요청이 끝난 연월일시, dur = 요청이 처리되기까지의 시간
		et, dur = "".join(tmp[0] + "T" + tmp[1]), float(tmp[2][:-1])
		et = datetime.datetime.fromisoformat(et)
		dur = datetime.timedelta(seconds = dur)
		print(et, dur)

